package operators;

public class Compound_5 {
public static void main(String[] args) {
	int num=100;
	System.out.println("Initial value is: "+num);
	
	num += 10;
	System.out.println("After adding 10: "+num);
	
	num -= 5;
	System.out.println("After subtracting 5: "+num);
	
	num *= 20;
	System.out.println("After multiplying by 20: "+num);
	
	num /= 2;
	System.out.println("After dividing by 2: "+num);
	
	num %= 1;
	System.out.println("After %1: "+num);
}
}
