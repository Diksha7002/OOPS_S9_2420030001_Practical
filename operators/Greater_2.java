package operators;

public class Greater_2 {
public static void main(String args[]) {
	int x=100;
	int y=1000;
	
	if(x>y) {
		System.out.println(x+" is greater.");
	}else if(y>x) {
		System.out.println(y+" is greater.");
	}else {
		System.out.println("Both x and y are equal.");
	}
}
}
